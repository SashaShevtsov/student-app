package by.iba.student.common;

public class Group {
	private int groupNumber;

	public Group() {
		super();
	}

	public Group(int groupNumber) {
		super();
		this.groupNumber = groupNumber;
	}

	public int getGroupNumber() {
		return groupNumber;
	}

	public void setGroupNumber(int groupNumber) {
		this.groupNumber = groupNumber;
	}
	
	
}
